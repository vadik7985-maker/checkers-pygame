"""
Модуль для хранения перечислений (Enums).

Этот модуль определяет перечисления (Enum), используемые в игре "Шашки".
Перечисления обеспечивают типобезопасность и читаемость кода.

Определяет:
    1. PieceType - тип шашки (простая или дамка)
    2. Player - игрок (белые или черные)

Используется во всех модулях игры для четкого определения состояний.
"""

from enum import Enum


class PieceType(Enum):
    """Перечисление типов шашек.

    Определяет возможные типы шашек на доске:

    Attributes:
        NONE: Отсутствие шашки (пустая клетка)
        MAN: Простая шашка
        KING: Дамка (шашка, достигшая противоположного края доски)
    """
    NONE = 0
    MAN = 1
    KING = 2


class Player(Enum):
    """Перечисление игроков.

    Определяет двух игроков в шашках:

    Attributes:
        WHITE: Игрок, играющий белыми шашками (ходит первым)
        BLACK: Игрок, играющий черными шашками
    """
    WHITE = 0
    BLACK = 1