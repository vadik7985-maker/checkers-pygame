"""
Модуль для хранения всех констант игры.

Этот модуль содержит все константы, используемые в игре "Шашки".
Константы разделены на категории для удобства использования.
Все значения измеряются в пикселях, секундах или RGB-кодах цветов.

Категории:
    1. Размеры окна и доски (BOARD_SIZE, SQUARE_SIZE, WIDTH, HEIGHT, FPS)
    2. Цвета элементов игры (DARK_WOOD, LIGHT_WOOD, BLACK_PIECE и др.)
    3. Настройки времени (INITIAL_TIME_MINUTES, INITIAL_TIME_SECONDS)
    4. Графические параметры (HIGHLIGHT_CAPTURE, PIECE_SHADOW)
"""

# размеры окна и доски
BOARD_SIZE = 8  # стандартный размер доски для шашек (8x8)
SQUARE_SIZE = 100  # размер одной клетки в пикселях
WIDTH = BOARD_SIZE * SQUARE_SIZE + 300  # общая ширина окна (доска + панель)
HEIGHT = BOARD_SIZE * SQUARE_SIZE + 50  # общая высота окна (доска + статусная панель)
FPS = 60  # кадров в секунду (для плавной анимации)

# цвета деревянной доски
DARK_WOOD = (89, 47, 27)  # темное дерево для темных клеток
LIGHT_WOOD = (210, 180, 140)  # светлое дерево для светлых клеток

# цвета шашек
BLACK_PIECE = (45, 45, 60)  # цвет черных шашек
WHITE_PIECE = (235, 235, 245)  # цвет белых шашек

# акцентные цвета (для выделения элементов)
ACCENT_GOLD = (255, 215, 0)  # золотой - для дамок, выделения
ACCENT_SILVER = (200, 200, 210)  # серебряный - для активного игрока
ACCENT_RED = (220, 80, 80)  # красный - для обязательного взятия
ACCENT_GREEN = (80, 200, 120)  # зеленый - для допустимых ходов
ACCENT_BLUE = (70, 130, 200)  # синий - для кнопок

# фоновые цвета
BACKGROUND = (30, 30, 40)  # основной фон окна
PANEL_COLOR = (40, 40, 55)  # цвет боковой панели
PANEL_ACCENT = (60, 60, 80)  # акцентный цвет панели
LIGHT_BLUE = (173, 216, 230)  # светлый синий для кнопок

# цвета текста и статусной панели
STATUS_BAR = (35, 35, 50)  # цвет нижней статусной панели
TEXT_LIGHT = (240, 240, 250)  # светлый текст
TEXT_DARK = (180, 180, 200)  # темный текст

# цвет для подсветки шашек, которые можно взять
HIGHLIGHT_CAPTURE = (255, 100, 100, 180)  # полупрозрачный красный

# цвет тени под шашкой
PIECE_SHADOW = (20, 20, 30)  # темный цвет для тени

# настройки времени
INITIAL_TIME_MINUTES = 7  # начальное время на партию (7 минут)
INITIAL_TIME_SECONDS = INITIAL_TIME_MINUTES * 60  # время в секундах